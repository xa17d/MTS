CREATE TABLE PATIENT 
	(ID INTEGER GENERATED BY DEFAULT AS IDENTITY (START WITH 1 INCREMENT BY 1) NOT NULL, 
	GUID			VARCHAR(36) NOT NULL,
	VERSION			INTEGER NOT NULL,
	
	NAMEGIVEN		LONGVARCHAR,
	NAMEFAMILY		LONGVARCHAR,
	BIRTHTIME		DATE,
	GENDER			INTEGER,
	WALKABLE		BOOLEAN,
	RESPIRATION		INTEGER,
	PERFUSION		INTEGER,
	MENTALSTATUS	INTEGER,
	PHASEOFLIFE		INTEGER,
	PLACEPOSITION	LONGVARCHAR,
	URGENCY			INTEGER,
	BLOODPRESSURESYSTOLIC	LONGVARCHAR,
	BLOODPRESSUREDIASTOLIC	LONGVARCHAR,
	PULSE			INTEGER,
	READYFORTRANSPORT	BOOLEAN,
	HOSPITAL		LONGVARCHAR,
	HEALTHINSURANCE	LONGVARCHAR,
	TREATMENT		INTEGER,
	
	PRIMARY KEY (ID));
	
CREATE TABLE BODYPART 
	(ID INTEGER GENERATED BY DEFAULT AS IDENTITY (START WITH 1 INCREMENT BY 1) NOT NULL, 
	PATIENT	INTEGER NOT NULL,
	KEY		VARCHAR(36) NOT NULL,
	VALUE	LONGVARCHAR,
	PRIMARY KEY (ID));
	
ALTER TABLE BODYPART ADD CONSTRAINT FK_BODYPART_PATIENT FOREIGN KEY (PATIENT) REFERENCES PATIENT (ID);