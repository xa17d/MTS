CREATE TABLE PATIENT 
	(ID INTEGER GENERATED BY DEFAULT AS IDENTITY (START WITH 1 INCREMENT BY 1) NOT NULL, 
	GUID			VARCHAR(36) NOT NULL,
	VERSION			INTEGER NOT NULL,
	
	PRIMARY KEY (ID));

CREATE TABLE PATIENTVERSION
	(ID INTEGER GENERATED BY DEFAULT AS IDENTITY (START WITH 1 INCREMENT BY 1) NOT NULL, 
	PATIENT			INTEGER NOT NULL,
	VERSION			INTEGER NOT NULL,
	
	NAMEGIVEN		LONGVARCHAR,
	NAMEFAMILY		LONGVARCHAR,
	BIRTHTIME		DATE,
	GENDER			VARCHAR(15),
	CATEGORY		VARCHAR(15),
	WALKABLE		BOOLEAN,
	RESPIRATION		VARCHAR(15),
	PERFUSION		VARCHAR(15),
	MENTALSTATUS	VARCHAR(15),
	PHASEOFLIFE		VARCHAR(15),
	SALVAGEINFO		LONGVARCHAR,
	PLACEPOSITION	LONGVARCHAR,
	URGENCY			INTEGER,
	BLOODPRESSURESYSTOLIC	INTEGER,
	BLOODPRESSUREDIASTOLIC	INTEGER,
	PULSE			INTEGER,
	READYFORTRANSPORT	BOOLEAN,
	HOSPITAL		LONGVARCHAR,
	HEALTHINSURANCE	LONGVARCHAR,
	TREATMENT		VARCHAR(15),
	
	PRIMARY KEY (ID));

	
CREATE TABLE BODYPART 
	(ID INTEGER GENERATED BY DEFAULT AS IDENTITY (START WITH 1 INCREMENT BY 1) NOT NULL, 
	PATIENTVERSION	INTEGER NOT NULL,
	KEY		VARCHAR(36) NOT NULL,
	VALUE	LONGVARCHAR,
	PRIMARY KEY (ID));

ALTER TABLE PATIENTVERSION ADD CONSTRAINT FK_PATIENTVERSION_PATIENT FOREIGN KEY (PATIENT) REFERENCES PATIENT (ID);
ALTER TABLE BODYPART ADD CONSTRAINT FK_BODYPART_PATIENTVERSION FOREIGN KEY (PATIENTVERSION) REFERENCES PATIENT (ID);